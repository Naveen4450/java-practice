import java.util.Scanner;
import java.lang.Math;
class mathop
{
	public static void main(String args[])
	{
		double d=Math.toRadians(90);
		System.out.println("sin of  90="+Math.sin(d));
		System.out.println("log of 2="+Math.log(2));
		System.out.println("2 power 5="+Math.pow(2,5));
		System.out.println("Square root of 4 is "+Math.sqrt(4));
		System.out.println("ceil of 5.8 is "+Math.ceil(5.8));
		System.out.println("floor of 4.5 is "+Math.floor(4.5));
		System.out.println("absolute value of -2 is"+Math.abs(-2));
		System.out.println("maximum of 56 and 30 is "+Math.max(56,30));
		System.out.println("minimum of 34 and 76 is "+Math.min(34,76));
	}
}